package Model;

/**
 *
 * @author PC
 */
public class Model_LG {

    private String maTaiKhoan;
    private String tenDangNhap;
    private String matKhau;
    private String chucVu;

    public Model_LG(String tenDangNhap, String matKhau) {
        this.tenDangNhap = tenDangNhap;
        this.matKhau = matKhau;
    }

    public Model_LG() {
    }

    public Model_LG(String maTaiKhoan, String tenDangNhap, String matKhau, String chucVu) {
        this.maTaiKhoan = maTaiKhoan;
        this.tenDangNhap = tenDangNhap;
        this.matKhau = matKhau;
        this.chucVu = chucVu;
    }

    public String getMaTaiKhoan() {
        return maTaiKhoan;
    }

    public void setMaTaiKhoan(String maTaiKhoan) {
        this.maTaiKhoan = maTaiKhoan;
    }

    public String getTenDangNhap() {
        return tenDangNhap;
    }

    public void setTenDangNhap(String tenDangNhap) {
        this.tenDangNhap = tenDangNhap;
    }

    public String getMatKhau() {
        return matKhau;
    }

    public void setMatKhau(String matKhau) {
        this.matKhau = matKhau;
    }

    public String getChucVu() {
        return chucVu;
    }

    public void setChucVu(String chucVu) {
        this.chucVu = chucVu;
    }

    public Object[] toDataRow_LG(){
        return new Object[]{this.getMaTaiKhoan(), this.getTenDangNhap(), this.getMatKhau(), this.getChucVu()};
    }
}
